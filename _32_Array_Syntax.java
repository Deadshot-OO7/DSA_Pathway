package DSA_Revision;

public class _32_Array_Syntax {
    public static void main(String[] args) {

        int[] marks = new int[3];

        marks[0] = 34;
        marks[1] = 45;
        marks[2] = 55;

        for (int i = 0; i < marks.length; i++) {
            System.out.println(marks[i]);
        }
    }
}
